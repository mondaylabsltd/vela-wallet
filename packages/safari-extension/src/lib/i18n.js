/** Resolve the locale to render in: the app's cached locale if we bundle it, else
 *  the browser language (exact, then base like 'pt'→'pt-BR'), else English. */
export function pickLocale(cached) {
  if (cached && MESSAGES[cached]) return cached;
  try {
    const nav = (typeof navigator !== 'undefined' && navigator.language) || 'en';
    if (MESSAGES[nav]) return nav;
    const base = nav.split('-')[0];
    const hit = LOCALES.find((l) => l === base || l.split('-')[0] === base);
    if (hit && MESSAGES[hit]) return hit;
  } catch (_) { /* ignore */ }
  return 'en';
}

/** Translate `key` in `locale`, filling `{name}` placeholders from `vars`. Falls back
 *  to English, then to the key itself, so a missing string never renders blank. */
export function t(key, locale, vars) {
  const table = MESSAGES[locale] || MESSAGES.en;
  let s = table[key];
  if (s == null) s = MESSAGES.en[key];
  if (s == null) return key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      s = s.replace(new RegExp('\\{' + k + '\\}', 'g'), String(vars[k]));
    }
  }
  return s;
}
