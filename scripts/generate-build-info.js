#!/usr/bin/env node
/**
 * Generates src/constants/build-info.ts with the current git commit hash.
 * Run automatically via the "prebuild" npm script.
 */
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const commitHash = execSync('git rev-parse --short HEAD', { encoding: 'utf8' }).trim();
const version = require('../package.json').version;

const out = path.join(__dirname, '..', 'src', 'constants', 'build-info.ts');
fs.writeFileSync(out, `// Auto-generated by scripts/generate-build-info.js — do not edit
export const APP_VERSION = '${version}';
export const GIT_COMMIT = '${commitHash}';
`);

console.log(`[build-info] v${version} (${commitHash})`);
